SELECT CAR_ID, CAR_TYPE, ROUND(DAILY_FEE*30*(100-DISCOUNT_RATE)/100) FEE
FROM CAR_RENTAL_COMPANY_CAR A
RIGHT JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN C USING (CAR_TYPE)
WHERE DURATION_TYPE = '30일 이상' AND (DAILY_FEE*30*(100-DISCOUNT_RATE)/100 BETWEEN 500000 AND 2000000) AND CAR_TYPE IN ('세단', 'SUV') 
AND CAR_ID NOT IN 
(
    SELECT CAR_ID
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE DATE_FORMAT(START_DATE, '%Y-%m-%d') BETWEEN '2022-11-01' AND '2022-11-30' OR DATE_FORMAT(END_DATE, '%Y-%m-%d') BETWEEN '2022-11-01' AND '2022-11-30' OR (DATE_FORMAT(START_DATE, '%Y-%m-%d') < '2022-11-01' AND DATE_FORMAT(END_DATE, '%Y-%m-%d') > '2022-11-30')
)
ORDER BY FEE DESC, CAR_TYPE, CAR_ID DESC;